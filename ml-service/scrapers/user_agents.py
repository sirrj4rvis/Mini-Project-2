def get_random_user_agent():
    try:
        from fake_useragent import UserAgent
        ua = UserAgent(platforms=['pc'], os=['windows', 'macos'])
        return ua.random
    except ImportError:
        return random.choice(USER_AGENTS)
    except Exception:
        return random.choice(USER_AGENTS)
